/**
 * Creates an SVG element from a path string.
 * Reuses the same factory that was previously in idle.ts (now removed).
 */
export const createSvgIcon = (
	svgPath: string,
	viewBox = "0 0 24 24",
	className = "kiosk-icon",
	size?: number | string,
): SVGSVGElement => {
	const svg = document.createElementNS("http://www.w3.org/2000/svg", "svg");
	svg.setAttribute("viewBox", viewBox);
	svg.setAttribute("class", className);

	if (size !== undefined) {
		const normalizedSize = typeof size === "number" ? `${size}` : size;
		svg.setAttribute("width", normalizedSize);
		svg.setAttribute("height", normalizedSize);
	}

	const path = document.createElementNS("http://www.w3.org/2000/svg", "path");
	path.setAttribute("d", svgPath);
	path.setAttribute("fill", "currentColor");

	svg.appendChild(path);
	return svg;
};

